const mathOperations = {
    add: function (a, b) {
        return a + b;
    },

    sub: function (a, b) {
        return a - b;
    },

    product: function (a, b) {
        return a * b;
    },

    division: function (a, b) {
        return a / b;
    }

};

module.exports = {
    mathOperations,
};